    <p>Que es codigo abierto? Es un concepto moderno, donde todo lo que se crea es publico. Eso trae algunas vantajas como:</p>
    <p>Transparencia, ya que cualquier otro desarrollador puede analisar el codigo.</p>
    <p>Liberdad, en cualquier momento puedes cerrar los servicios con nosotros y buscar un nuevo professional que despegué la aplicación.</p>
    <p>Confianza, si hubier algun desacuerdo con el cliente, el siempre va tener el poder agarrar el codigo y hacer las modificaciones que desea.</p>
</div>
